;CLEAR RAM AREA FOR TEMP X/Y POSITIONS
	MOVI	RAMXY,A0
	CLR	A3
	MOVK	20,A1
LP9:	MOVE	A3,*A0+,L
	DSJS	A1,LP9

	MOVI	[60,60],A0		;SOME VALUE TO INIT TABLE WITH
					;NOTHING WILL ORIGINATE AT 20/20
	MOVI	RAMXY,A1
	MOVE	A0,*A1+,L
	MOVE	A1,@KEEPXY,L
