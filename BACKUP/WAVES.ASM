;SCRIPTPLAY - AUTOMATIC PLAYER MOVEMENT
;A8=1 OR 2 FOR PLAYER NUMBER
;A10=0 TO RUN IN FROM BOTTOM,1=FROM LEFT, 2=FROM LOWER RIGHT
SCRIPTPLAY:
	SLEEPK	3
	SLL	5,A10
	ADDI	INLST,A10
	MOVE	*A10,A9,L
	MMTM	SP,A8,A9
	CMPI	FROMRGT,A9
	JRZ	PPO
	MOVE	A8,A9
	MOVI	35,A10			;TIME RINGS ARE ON
	CMPI	1,A9
	JRZ	PP1
	CREATE	RNG2PID,RINGS
	JRUC	PPO
