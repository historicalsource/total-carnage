	DEC	A8
	SLL	5,A8
	MOVE	A8,A11
	ADDI	CTRLS,A8
	MOVE	*A8,A8,L

	ADDI	PLYRPRCS,A11
	MOVE	*A11,A11,L
	MOVE	*A11(LEG_PRC),A11,L
