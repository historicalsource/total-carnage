ANI_STEEL
;A8=STEEL DOOR IMAGE FROM BACKGROUND UPDATER
;ANIMATE, THEN KILL OFF ONCE IT IS OFF SCREEN
	CLR	A0
	MOVE	A0,*A13(PDATA)		;GRUNTS SPAWNED FROM THIS TRAP DOOR
	SLEEP	4*60
ANIST
	MOVI	20,B0
	MOVI	2*60,B1
	CALLA	RANGRAND
	CALLA	PRCSLP

	MOVE	@WORLDTLY,A0,L
	ADDI	[250,0],A0
	MOVE	*A8(OYVAL),A1,L
	CMP	A0,A1
	JRC	ANI_S2
;FALLS IN HERE IF PIT IS DOWN BELOW SCREEN
	MOVE	A8,A0
	CALLA	DELOBJ
	DIE
ANI_S2
;CHECK TO SEE IF I CAN GENERATE A GRUNT
;IF SO, OPEN DOOR AND SEND X # OF GRUNTS OUT
;SEND GRUNT AT PROPER DIRECTION
	MOVE	@HULK_CNT,A2		;MOVE BYTE (# HULKS ON SCRN) TO A2
	MOVE	@MAXHULK,A3
	SUBK	7,A3
	CMP	A3,A2			;HULKS MAXED OUT?
	JRGT	ANIST
	MOVE	*A13(PDATA),A0	
	CMPI	75,A0
	JRGT	ANIST
	MOVI	PITDR2,A1		;HALFWAY
	MOVE	*A8(OFLAGS),A4
	CALLA	ANI
	SLEEPK	2
	MOVE	A8,A0
	CALLA	OBJOFF			;OPEN ALL THE WAY
	MOVE	@PCNT,A9
	ANDI	07H,A9
	INC	A9
ANS3	SLEEPK	1
	MOVE	@HALT,A0
	JRNZ	ANS3
;TURN ON GRUNT AND SEND HIM OUT!
MJP	CREATE	HULKPID,JUMPOUT
	MOVE	@HTYPE,A1
	MOVE	A1,*A0(HULKTYP)
	MOVE	*A13(PDATA),A0
	INC	A0
	MOVE	A0,*A13(PDATA)		;HULKS RELEASED FROM THIS PIT!
	MOVK	1,B0
	MOVK	23,B1
	CALLA	RANGRAND
	CALLA	PRCSLP
	DSJS	A9,MJP
	SLEEPK	15
	MOVE	A8,A0
	CALLA	OBJON			;HALFWAY
	SLEEPK	2
	MOVI	PITDR1,A1
	MOVE	*A8(OFLAGS),A4
	CALLA	ANI			;CLOSED
	JRUC	ANIST

