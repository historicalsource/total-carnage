LUP_TP:
;TOP OF TANK LOGIC
	MOVE	@HALT,A0
	JRZ	TMV
	SLEEPK	1
   	JRUC	LUP_TP

TMV
	MOVE	*A13(CNTR),A3
	ANDI	07H,A3
	DEC	A3
	JRNZ	TMV3
;SWAP THIS MINE LAYER OID BETWEEN PLAYER/HELPER AND MINE LAYER
	MOVK	1,A3
	MOVI	CLSPLYR|TYPHLPR|SUBLAY,A1
	MOVE	*A8(OID),A0
	ANDI	0FFFFH,A0
	CMPI	CLSENMY|TYPLAYR,A0
	JRZ	TMV2
	MOVK	3,A3
	MOVI	CLSENMY|TYPLAYR,A1
TMV2	MOVE	A1,*A8(OID)
TMV3
	MOVE	A3,*A13(CNTR)
	MOVE	*A13(CFLAGS),A0
	BTST	0,A0
 	JRZ	NOSPN
;THIS TANK IS SPINNING, DO SPIN
	MOVE	*A13(SK_DIR),A1
	MOVE	*A13(DIR),A0,W
	CALLR	WAY
	JRC	DIT			;INC
;DEC=CLRC
	CLR	A1			;MOVI	0,A1
	MOVE	A1,*A13(WAY01)
	JRUC	DIT0
DIT	MOVK	1,A1
	MOVE	A1,*A13(WAY01)
DIT0
	MOVE	A0,*A13(DIR),W
	CALLR	GET_STAND		;DOES ANI OF CORRECT VIEW
	MOVE	*A13(SK_DIR),A1
	MOVE	*A13(DIR),A0,W
	CMP	A0,A1
	JREQ	GOODNEWS
;ANI IN NEW STAND/SPIN DIR
	SLEEPK	4
	JRUC	LUP_TP

GOODNEWS:
	SLEEPK	20
	CALLR	GET_RUN			;ZEROS STAND FLAG ALSO
	MOVE	*A13(DIR),A0
	CALLA	INTO12			;GET PROPER VELOCITY FOR THIS DIR
	SLEEPK	1
	JRUC	LUP_TP

NOSPN:	MOVE	*A13(DIS),A0		;UPDATE DISTANCE COUNT
	DEC	A0
	MOVE	A0,*A13(DIS)		;STUFF NEW COUNT
	JRNZ	DOTNK			;BR=CONTINUE CURRENT DIRECTION
;TIME TO GET NEW DISTANCE AND NEW DIRECTION
	MOVE	*A13(MYPATRN),A10,L
	MOVE	*A10+,A11,W		;GET NEW DIRECTION
	JRZ	PATDN			;BR=PATTERN FINISHED
	CLR	A0
	MOVE	A0,*A8(OXVEL),L
	MOVE	A0,*A8(OYVEL),L
	SLEEPK	20
	MOVE	*A10+,A2,W		;GET NEW DIS
	MOVE	*A13(SPDY),A3
	JRZ	NOSPD
;SPEEDY MINE LAYER
