	MOVI	TEXTPAL,A0
	MOVI	FadeIn,A11
	CALLA	FADE_ONLY
	SLEEP	60*5
	MOVI	TEXTPAL,A0
	MOVI	FadeOut,A11
	CALLA	FADE_ONLY
	SLEEP	20
	CALLA	CLR_SCRN
	MOVI	TEXTPAL,A0
	MOVI	BRIGHT_LIST,A11
	CALLA	FADE_ONLY
	SLEEP	1
	RETP

	
