**************************************************************************
*								         *
* CREDIT_THINGY								 *
* 									 *
* RUN THE CREDIT SEQUENCE						 *
* 									 *
* IN:  NONE								 *
* OUT: NONE								 *
*								         *
**************************************************************************

CREDIT_THINGY
	CALLA	WIPEOUT		     
	CALLA	SINGLE_PLANE		
	PAGEON

	MOVK	1,A0
	MOVE	A0,@ON_HSTD,W		
	
	CLR	A0
	MOVE	A0,@SCRNTL,L		

	DISPON

	MOVK	3,A14
	MOVE	A14,@GAMERASE,W

	CREATE	PID_ANIMP,ANIMP		

	CREATE	PID_IND,DEL_CHARS_PROC

	MOVI	CRED_LIST,A8
	JSRP	DO_TEXT

	SLEEP	60*5

	CLR	A14
	MOVE	A14,@GAMERASE,W
	PAGEOFF

	JSRP	COPYR

	RETP

**************************************************************************
*								         *
* STORY_THINGY								 *
* 									 *
* RUN THE CREDITS							 *
* 									 *
* IN:  A8, CREDIT LIST 							 *
* OUT: NONE								 *
*								         *
**************************************************************************

DO_TEXT
	MOVE	A8,*A13(LIST_PTR),L
TEXT_LP
	MOVE	*A13(LIST_PTR),A1,L
	MOVE	*A1+,A14,W
	MOVE	A14,*A13(LINE_DELAY),W
	MOVE	*A1+,A8,L
	MOVE	*A1+,A3,L
	JRZ	TEXT_DONE			; BR = NO MORE TEXT
	MOVE	A1,*A13(LIST_PTR),L
	CALLA	LM_SETUP
	MOVE	A3,A8
	JSRP	PRINTF
	PUSHST
	DINT
	MOVI	OID_CUSTOM,A0
	CALLA	EXISTOBJ_ALL
	JRZ	TEXT_WAIT			; BR = TIME TO NAP
	MOVE	A0,A8
TEXT_MOVE
	MOVI	TEXTOID,A14
	MOVE	A14,*A8(OID),L
	MOVI	TEXT_VEL,A14
	MOVE	A14,*A8(OYVEL),L
	MOVI	OID_CUSTOM,A0
	CALLA	NEXTOBJ
	JRZ	TEXT_MOVE			; BR = MORE TEXT TO MOTIVATE
TEXT_WAIT
	POPST	
	MOVE	*A13(LINE_DELAY),A14,W
	JRZ	TEXT_LP				; BR = MORE CREDITS TO DO
	SLEEPR	A14
	JRUC	TEXT_LP
TEXT_DONE
	RETP


**************************************************************************
*								         *
* NEXTOBJ								 *
* 									 *
* FIND NEXT OBJECT IN THE LIST						 *
* 									 *
* IN:  A8, CURRENT OBJECT						 *
*      A0, OID TO MATCH							 *
* 									 *
* OUT: Z FLAG SET -> A8, NEXT OBJECT					 *
*      Z FLAG CLR -> NO MORE OBJECTS 					 *
*								         *
**************************************************************************

NEXTOBJ
	MOVE	*A8(OLINK),A8,L
	MOVE	*A8(OID),A14,W
	CMP	A0,A14
	RETS

**************************************************************************
*								         *
* DEL_CHARS_PROC							 *
* 									 *
* DELETE CHARACTERS AFTER THEY SCROLL OFF THE SCREEN			 *
* 									 *
* IN:  NONE								 *
* OUT: NONE								 *
*								         *
**************************************************************************

DEL_CHARS_PROC
	MOVI	FGLISTS,A0
	MOVE	A0,A8
CHECK_SCRN_CHAR
	MOVE 	*A8,A8,L
	CMP	A0,A8
	JRNE	OSC_CONT		;BR = NOT DONE CHECKING THE LIST
	SLEEP	1
	JRUC	DEL_CHARS_PROC	
OSC_CONT
	MOVE	*A8(OYPOS),A14,W
	CMPI	DEL_Y_LIMIT,A14
	JRGT	CHECK_SCRN_CHAR	    ;BR = CHARACTER STILL ON SCREEN (SORT OF)
	MOVE	*A8(OLINK),A1,L
	CALLA	DELETE_OBJ
	MOVE	A1,A8
	JRUC	CHECK_SCRN_CHAR


COPYR
	MOVI	TEXTPAL,A0
	MOVI	BLACK_LIST,A11
	CALLA	FADE_ONLY
	SLEEP	1
	MOVI	COPYRIGHTS,A8
